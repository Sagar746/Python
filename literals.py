# Literals in python is data type that can hold any  value type like string, number, boolean and more
# Literal is a raw data given in a variable.

# Numeric Literals
a = 0b1010
print(a)

float1 = 10.5
print(float1)

x = 3+4j
print(x,x.imag, x.real)


# String Literals
string = 'This is python'
strings = "This is Python"
char = "C"
multiline_str = """ This is python code. """
unicode = u"\U0001f600"
raw = r"raw \n string"
print(string, strings, char, multiline_str, unicode, raw)


# Boolean Literals
a = True +4
b = False+10
print(a, b)


# Special Literals
a = None # absence of anything
print(a)

# we don't declare variable in python so make declaration of variable we use "None"
